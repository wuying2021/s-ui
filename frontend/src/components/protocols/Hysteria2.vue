<template>
  <v-card subtitle="Hysteria2">
    <v-row v-if="direction == 'in'">
      <v-col cols="12" sm="6" md="4">
        <v-text-field
        label="Masquerade"
        hide-details
        v-model="data.masquerade">
        </v-text-field>
      </v-col>
      <v-col cols="12" sm="6" md="4">
        <v-switch v-model="data.ignore_client_bandwidth" color="primary" label="Ignore Client Bandwidth" hide-details></v-switch>
      </v-col>
    </v-row>
    <v-row v-else>
      <v-col cols="12" sm="6" md="4">
        <v-text-field
        label="Password"
        hide-details
        v-model="data.password">
        </v-text-field>
      </v-col>
      <v-col cols="12" sm="6" md="4">
        <Network :data="data" />
      </v-col>
    </v-row>
    <v-row v-if="!data.ignore_client_bandwidth">
      <v-col cols="12" sm="6" md="4">
        <v-text-field
        label="Uplink Limit"
        hide-details
        type="number"
        suffix="Mbps"
        min="0"
        v-model.number="up_mbps">
        </v-text-field>
      </v-col>
      <v-col cols="12" sm="6" md="4">
        <v-text-field
        label="Downlink Limit"
        hide-details
        type="number"
        suffix="Mbps"
        min="0"
        v-model.number="down_mbps">
        </v-text-field>
      </v-col>
    </v-row>
    <v-row v-if="data.obfs">
      <v-col cols="12" sm="6" md="4">
       <v-text-field
        label="obfs Password"
        hide-details
        v-model="data.obfs.password">
        </v-text-field>
      </v-col>
    </v-row>
    <v-card-actions>
      <v-spacer></v-spacer>
      <v-menu v-model="menu" :close-on-content-click="false" location="start">
        <template v-slot:activator="{ props }">
          <v-btn v-bind="props" hide-details>Hysteria2 Options</v-btn>
        </template>
        <v-card>
          <v-list>
            <v-list-item>
              <v-switch v-model="optionObfs" color="primary" label="Obfs" hide-details></v-switch>
            </v-list-item>
          </v-list>
        </v-card>
      </v-menu>
    </v-card-actions>
  </v-card>
</template>

<script lang="ts">
import Network from '@/components/Network.vue'

export default {
  props: ['direction', 'data'],
  data() {
    return {
      menu: false,
    }
  },
  computed: {
    down_mbps: {
      get() { return this.$props.data.down_mbps?? 0 },
      set(newValue:number) { this.$props.data.down_mbps = newValue>0 ? newValue : undefined }
    },
    up_mbps: {
      get() { return this.$props.data.up_mbps?? 0 },
      set(newValue:number) { this.$props.data.up_mbps = newValue>0 ? newValue : undefined }
    },
    optionObfs: {
      get(): boolean { return this.$props.data.obfs != undefined },
      set(v:boolean) { this.$props.data.obfs = v ? { type: "salamander", password: "" } : undefined }
    }
  },
  components: { Network }
}
</script>